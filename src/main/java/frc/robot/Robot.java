package frc.robot;

import edu.wpi.first.cameraserver.CameraServer;
import edu.wpi.first.wpilibj.TimedRobot;
import edu.wpi.first.wpilibj2.command.Command;
import edu.wpi.first.wpilibj2.command.CommandScheduler;
import frc.robot.subsystems.DriveCamera;

public class Robot extends TimedRobot {
  private Command autonomousCommandObj;

  private RobotContainer robotContainerObj;

  // private DriveCamera camera = new DriveCamera(); 

  @Override
  public void robotInit() {
    // CameraServer.addCamera(camera.driveCamera);
    CameraServer.startAutomaticCapture();
    robotContainerObj = new RobotContainer();
  }

  @Override
  public void robotPeriodic() {
    CommandScheduler.getInstance().run();
  }

  @Override
  public void disabledInit() {}

  @Override
  public void disabledPeriodic() {}

  @Override
  public void disabledExit() {}

  @Override
  public void autonomousInit() {
    autonomousCommandObj = robotContainerObj.getAutonomousCommand();

    if (autonomousCommandObj != null) {
      autonomousCommandObj.schedule();
    }
  }

  @Override
  public void autonomousPeriodic() {}

  @Override
  public void autonomousExit() {}

  @Override
  public void teleopInit() {
    if (autonomousCommandObj != null) {
      autonomousCommandObj.cancel();
    }
  }

  @Override
  public void teleopPeriodic() {}

  @Override
  public void teleopExit() {}

  @Override
  public void testInit() {
    CommandScheduler.getInstance().cancelAll();
  }

  @Override
  public void testPeriodic() {}

  @Override
  public void testExit() {}
}
